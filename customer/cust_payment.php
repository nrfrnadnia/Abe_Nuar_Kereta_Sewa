<?php
// Establish connection to the rental car database
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "keretasewa_db";

$conn = new mysqli($servername, $username, $password, $dbname);

if ($conn->connect_error) {
    die("Database connection failure: " . $conn->connect_error);
}

// Handle Payment Submission
if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['submit_payment'])) {
    $bookID = intval($_POST['bookID']);
    $custID = intval($_POST['custID']);
    $carID = intval($_POST['carID']);
    $dayTotal = intval($_POST['dayTotal']);
    $timeTotal = intval($_POST['timeTotalMins']);
    $payTotal = floatval($_POST['payTotal']);
    
    // Handle File Upload for Payment Proof
    $payProofPath = 'imgweb/default-pay.jpg';
    if (isset($_FILES['payProof']) && $_FILES['payProof']['error'] == UPLOAD_ERR_OK) {
        $target_dir = "imgweb/";
        if (!is_dir($target_dir)) mkdir($target_dir, 0777, true);
        
        $fileName = time() . "_" . basename($_FILES["payProof"]["name"]);
        $target_file = $target_dir . $fileName;
        
        if (move_uploaded_file($_FILES["payProof"]["tmp_name"], $target_file)) {
            $payProofPath = $target_file;
        }
    }

    // Insert into Payment Table
    $insert_payment = "INSERT INTO payment (bookID, custID, carID, payTotal, dayTotal, timeTotal, payProof) 
                       VALUES ($bookID, $custID, $carID, $payTotal, $dayTotal, $timeTotal, '$payProofPath')";
    
    if ($conn->query($insert_payment)) {
        header("Location: cust_receipt.php?bookID=" . $bookID);
        exit();
    } else {
        echo "<script>alert('Error submitting payment: " . $conn->error . "');</script>";
    }
}

// Get the specific booking ID
$bookID = isset($_GET['bookID']) ? intval($_GET['bookID']) : (isset($_POST['bookID']) ? intval($_POST['bookID']) : 1);

// Read the penalty rate generated by the admin panel
$penaltyFile = '../admin/penalty_rate.txt';
$penaltyRate = file_exists($penaltyFile) ? floatval(file_get_contents($penaltyFile)) : 10.00;

// Fetch booking, customer, car, and ACTUAL return details from return_update
$query = "SELECT b.*, c.custName, c.custIC, c.contactno, c.custEmail, 
                 car.carBrand, car.carModel, car.carPlate, car.carPrice,
                 ru.returnDate as actualReturnDate, ru.returnTime as actualReturnTime
          FROM booking b
          JOIN customer c ON b.custID = c.custID
          JOIN car ON b.carID = car.carID
          LEFT JOIN return_update ru ON b.bookID = ru.bookID
          WHERE b.bookID = $bookID";

$result = $conn->query($query);

if ($result && $result->num_rows > 0) {
    $row = $result->fetch_assoc();
    
    // 1. Create DateTime objects for Pickup and ACTUAL Return
    $pickupDT = new DateTime($row['pickupDate'] . ' ' . $row['pickupTime']);
    
    // Fallback to original booking date if return_update is missing for any reason
    $actualRetDate = !empty($row['actualReturnDate']) ? $row['actualReturnDate'] : $row['returnDate'];
    $actualRetTime = !empty($row['actualReturnTime']) ? $row['actualReturnTime'] : $row['returnTime'];
    
    $returnDT = new DateTime($actualRetDate . ' ' . $actualRetTime);
    $expectedReturn = new DateTime($row['returnDate'] . ' ' . $row['returnTime']);

    // 2. Calculate the exact time difference
    $interval = $pickupDT->diff($returnDT);
    
    // Convert the entire duration into total minutes for accurate conversion
    $totalMinutes = ($interval->days * 24 * 60) + ($interval->h * 60) + $interval->i;
    
    // Round up to the nearest whole hour (e.g., 2 hours 15 mins becomes 3 hours)
    $totalHoursToCharge = ceil($totalMinutes / 60);
    if ($totalHoursToCharge < 1) $totalHoursToCharge = 1;
    
    // 3. Calculate Total Price (Strictly Hourly)
    $carPricePerHour = floatval($row['carPrice']);
    $baseTotal = $totalHoursToCharge * $carPricePerHour;
    $baseDescrp = "RM " . number_format($carPricePerHour, 2) . "/hr &times; $totalHoursToCharge hrs";
    
    // 4. Format for display (Days and Hours)
    $displayDays = floor($totalHoursToCharge / 24);
    $displayHours = $totalHoursToCharge % 24;
    
    $totalDaysDisplay = $displayDays . " Day(s)";
    $totalHoursDisplay = $displayHours . " Hour(s)";

    // --- PENALTY CALCULATION (If Returned Late) ---
    $overdueHours = 0;
    $penTotal = 0;
    if ($returnDT > $expectedReturn) {
        $overdueDiff = $returnDT->getTimestamp() - $expectedReturn->getTimestamp();
        $overdueHours = ceil($overdueDiff / 3600);
        $penTotal = $overdueHours * $penaltyRate;
    }
    
    $penDescrp = "RM " . number_format($penaltyRate, 2) . "/hr &times; $overdueHours hrs";
    
    $grandTotal = $baseTotal + $penTotal;

} else {
    die("Invalid booking ID. Please ensure the reference number is correct.");
}
