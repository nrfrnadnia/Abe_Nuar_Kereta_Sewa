  <script>
    let dynamicResourcePayload = null;

    function openSystemExplorer() {
      document.getElementById('hidden-file-input').click();
    }

    document.getElementById('hidden-file-input').addEventListener('change', function(element) {
      const selectedFileReference = element.target.files[0];
      if (selectedFileReference) {
        if (dynamicResourcePayload) {
          URL.revokeObjectURL(dynamicResourcePayload);
        }
        
        dynamicResourcePayload = URL.createObjectURL(selectedFileReference);
        
        const stateNode = document.getElementById('statusLabel');
        stateNode.style.color = "#7b91f9"; 
        stateNode.innerText = `✓ Document selected: ${selectedFileReference.name}`;
      }
    });

    function evaluateSubmission() {
      const stateNode = document.getElementById('statusLabel');
      const fileInput = document.getElementById('hidden-file-input');
      
      if (!fileInput.value) {
        stateNode.classList.remove('vibrate');
        void stateNode.offsetWidth; 
        stateNode.style.color = "#dc2626";
        stateNode.innerText = "*Please upload your payment file/receipt before attempting to submit.";
        stateNode.classList.add('vibrate');
        return false;
      }
      return true;
    }

    function displayPreviewWindow() {
      if (!dynamicResourcePayload) {
        alert("Please upload your payment file/receipt first before attempting preview.");
        return;
      }

      const interactiveCanvasTarget = document.getElementById('previewSurfaceNode');
      interactiveCanvasTarget.innerHTML = `<img src="${dynamicResourcePayload}" alt="Receipt Document Payload Frame">`;
      
      document.getElementById('previewContainerOverlay').classList.add('active');
    }

    function hidePreviewWindow() {
      document.getElementById('previewContainerOverlay').classList.remove('active');
    }

    window.addEventListener('click', (event) => {
      const containerOverlay = document.getElementById('previewContainerOverlay');
      if (event.target === containerOverlay) {
        hidePreviewWindow();
      }
    });
  </script>

